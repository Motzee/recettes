<h2>Roses des sables</h2>

<section>
	<aside>
    <h3>Ingrédients :</h3>
		<ul>
			<li>125gr de chocolat à pâtisser cassés en carreaux</li>
			<li>125gr de chocolat praliné cassés en carreaux</li>
            <li>250gr de végétaline (1/2 pain) hachés en gros bouts</li>
            <li>200gr de sucre glace</li>
            <li>450gr de pétales de céréales type corn flakes</li>
		</ul>
        <p><em>recette proposée par : Maëlle</em></p>
	</aside>
	<p>Dans un bain marie à feu doux, mettre à fondre le chocolat et le praliné en remuant régulièrement (si besoin, ajouter un bout de végétaline pour aider à fondre).</p>
    <p>Lorsque le chocolat est onctueux, ajouter les morceaux de végétaline et continuer de remuer jusqu'à leur fonte totale.</p>
    <p>Hors du feu, rajouter le sucre glace tamisé et mélanger jusqu'à dissolution.</p>
    <p>Dans un très grand saladier, verser la moitié des céréales, puis les napper du mélange chocolaté. Remuer pour que tout s'imprègne bien, puis rajouter le reste descéréales et remuer à nouveau jusqu'à ce que tout soit bien nappé de chocolat.</p>
    <p>Recouvrir d'une feuille d'aluminium différents plats (assiettes, petits plateaux), et y déposer à l'aide de deux cuillères à soupe des petits tas espacés.</p>
    <p>Laisser figer les roses des sables quelques heures dans le réfrigérateur avant de commencer à les déguster. Elles pourront se conserver au frais plusieurs jours, dans une boite les protégeant de l'humidité.</p>
    
